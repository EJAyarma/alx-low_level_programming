
/**
 * op_add - Adds two numbers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two numbers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two numbers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides a number by another
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Adds two numbers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
